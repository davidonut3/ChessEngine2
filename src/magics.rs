/// Generates the sliding attacks array
pub fn generate_sliding_attacks() -> Box<[u64; ATTACK_ARRAY_SIZE]> {
    let mut result = Box::new([0; ATTACK_ARRAY_SIZE]);

    let mut square = 1u64;
    for _ in 0..64 {
        let square_index = square.trailing_zeros() as usize;
        let mask = ROOK_MASKS[square_index];
        let offset = ROOK_OFFSETS[square_index];

        for i in 0..2_usize.pow(mask.count_ones()) {
            let occupied = unsafe { _pdep_u64(i as u64, mask) };
            let attacks = generate_rook_moves(square, occupied, EMPTY);
            let index = offset + i;

            result[index] = attacks;
        }

        square <<= 1;
    }

    square = 1u64;
    for _ in 0..64 {
        let square_index = square.trailing_zeros() as usize;
        let mask = BISHOP_MASKS[square_index];
        let offset = BISHOP_OFFSETS[square_index];

        for i in 0..2_usize.pow(mask.count_ones()) {
            let occupied = unsafe { _pdep_u64(i as u64, mask) };
            let attacks = generate_bishop_moves(square, occupied, EMPTY);
            let index = offset + i;

            result[index] = attacks;
        }

        square <<= 1;
    }

    result
}

/// Generates pseudo-legal moves for the rook
pub fn generate_rook_moves(square: u64, occupied: u64, team: u64) -> u64 {
    let index = square.trailing_zeros() as usize;
    let blockers = occupied & ROOK_MASKS[index];

    let rank = get_rank(square);
    let file = get_file(square);

    let mut result = EMPTY;
    let mut move_to: u64;

    move_to = square >> 1;
    while move_to & rank != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to >>= 1;
    }

    move_to = square << 1;
    while move_to & rank != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to <<= 1;
    }

    move_to = square >> 8;
    while move_to & file != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to >>= 8;
    }

    move_to = square << 8;
    while move_to & file != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to <<= 8;
    }
    
    result & !team
}

/// Generates pseudo-legal moves for the bishop
pub fn generate_bishop_moves(square: u64, occupied: u64, team: u64) -> u64 {
    let index = square.trailing_zeros() as usize;
    let blockers = occupied & ROOK_MASKS[index];

    let mut result = EMPTY;
    let mut move_to: u64;

    move_to = square >> 9;
    while move_to & FILE_A != 0 && move_to != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to >>= 9;
    }

    move_to = square << 9;
    while move_to & FILE_H != 0 && move_to != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to <<= 9;
    }

    move_to = square >> 7;
    while move_to & FILE_H != 0 && move_to != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to = move_to >> 7;
    }

    move_to = square << 7;
    while move_to & FILE_A != 0 && move_to != 0 && move_to & blockers == 0 {
        result |= move_to;
        move_to <<= 7;
    }
    
    result & !team
}

pub fn get_rook_attack(square: u64, occupied: u64, team: u64) -> u64 {
    let sliding_attacks = SLIDING_ATTACKS.get().unwrap();

    let square_index = square.trailing_zeros() as usize;
    let mask = ROOK_MASKS[square_index];
    let offset = ROOK_OFFSETS[square_index];

    let pext = unsafe { _pext_u64(occupied, mask) as usize };

    let index = pext + offset;
    let attacks = sliding_attacks[index];

    attacks & !team
}

pub fn get_bishop_attack(square: u64, occupied: u64, team: u64) -> u64 {
    let sliding_attacks = SLIDING_ATTACKS.get().unwrap();

    let square_index = square.trailing_zeros() as usize;
    let mask = BISHOP_MASKS[square_index];
    let offset = BISHOP_OFFSETS[square_index];

    let pext = unsafe { _pext_u64(occupied, mask) as usize };

    let index = pext + offset;
    let attacks = sliding_attacks[index];

    attacks & !team
}

pub fn get_queen_attack(square: u64, occupied: u64, team: u64) -> u64 {
    get_rook_attack(square, occupied, team) | get_bishop_attack(square, occupied, team)
}
