/*
To generate pseudo-legal moves/attacks, we use lookup tables. For pawns, kings and knights, we have simple tables with 64 entries.
For rooks and bishops, we use a large array that we index using the PEXT/PDEP instructions: https://www.chessprogramming.org/BMI2.
Since these instructions cannot be checked properly by Rust, we have to use the unsafe environment. Also, not every CPU has access
to these instructions, so we have to check at compile-time whether these features are enabled.

To use the large array, we allocate the size of the array and fill it with 0's. Then, at the start of the program, we populate the
array with the init function. Whenever we want to access the array, we index it by adding the offset to the starting pointer of the
array, and getting the value at this address in memory. This is unsafe, since Rust cannot guarantee that the data is not being changed
or accessed by multiple threads at the same time. However, we write the data once and only read after, it is safe.
*/

